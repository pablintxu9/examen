#numeros del 1 al 5 sumados for
a=0
for i in range(1,6):
    a=a+i 
print(a)

#imprimir elementos de una lista
lista=["rojo","verde","azul","amarillo"]
for i in lista:
    print(i)

#numeros pares del 0 al 10
for i in range(0,11,2):
    if i%2==0:
        print(i)

#cuadrado usando for
